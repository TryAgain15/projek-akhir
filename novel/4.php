    <h4>Autumn In Paris</h4>
    <h4>Illana Tan</h4>
    <p>Sinopsis : Cinta datang dengan cara yang berbeda-beda. Tidak peduli siapa, dimana, atau mengapa. Itulah yang dialami gadis blasteran Indonesia-Prancis, Tara Dupont. Ia bukanlah wanita berkulit putih susu, bermata biru, atau berambut pirang seperti wanita Eropa kebanyakan. Ia memiliki wajah asia dan bermata abu-abu. Gadis yang berprofesi sebagai penyiar radio ini memiliki penyakit yang sangat sulit dihilangkan, yaitu rasa ingin tahu. Sifatnya inilah yang akhirnya membawa Tara menemukan cintanya.

        Hidup bergelimang harta tidak membuat hidup seseorang menjadi bahagia. Begitu juga dengan Tara. Ayahnya, Jean-Daniel Dupont merupakan seorang pengusaha kaya di Prancis. Sementara Ibunya adalah orang Indonesia. Mereka bercerai secara  baik-baik beberapa tahun lalu. Beruntunglah, ada seseorang yang dapat mengisi hari-harinya, dialah Sebastien Giraudeau, kakak angkatnya. Mereka berdua memiliki kesamaan nasib. Begitulah yang sering Sebastien katakan jika ada orang bertanya mengapa Ia dan Tara dekat. Ia menganggap Tara seperti adiknya. Begitu juga yang dirasakan Tara, Ia akan senang jika ada Sebastien.</p>
