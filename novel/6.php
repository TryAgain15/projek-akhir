    <h4>Winter In Tokyo</h4>
    <h4>Illana Tan</h4>
    <p>Sinopsis : bercerita tentang Ishida Keiko dan Nishimura Kazuto. Keiko adalah seorang wanita blesteran Indonesia-Jepang yang tinggal di sebuah apartemen di pinggiran Kota Tokyo. Saat musim dingin tiba, Keiko bertemu dan menjalin hubungan dengan Kazuto, tetangga barunya yang kembali ke Jepang setelah 10 tahun tinggal di Amerika.

        Karena terus bersama-sama, benih cinta pun muncul di hati mereka. Tapi, Keiko justru memilih Kitano Akira, cinta pertamanya, ketimbang Kazuto. Sebuah kejadian menyebabkan Kazuto hilang ingatan, disaat itu Keiko merasakan kehilangan seseorang yang sangat berarti dan telah menjadi bagian hidupnya.</p>
