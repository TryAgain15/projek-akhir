    <h4>ARTHA</h4>
    <h4>Bayu Permana</h4>
    <p>Sinopsis : Kalau kata Agatha, Arkan itu Cabe-Man. Cowok dengan mulut sepedas cabai, sangat pintar menari serta memiliki fisik yang menawan, setengah cantik dan setengah ganteng.

        Agatha menjuluki dirinya sendiri Wonder Woman, yang akan selalu kuat menghadapi tiap goresan takdir yang terjadi.
        
        Arkan itu menyebalkan, sering mencerca tanpa pikir panjang, terlalu jujur. Membuat Agatha keki sendiri, membuat Agatha baper sendiri.
        
        Namun, adanya Arkan seperti memberi warna baru di hidup Agatha. Hadirnya Arkan bagai penerang di gelapnya dunia Agatha. Arkan adalah Baskara yang hadir di hidupnya yang kelam.</p>
