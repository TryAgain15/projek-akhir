    <h4>5 cm </h4>
    <h4>Dhonny Dirgantoro</h4>
    <p>Sinopsis : Novel ini menceritakan kisah persahabatan lima anak muda yaitu Genta, Arial, Riani, Zafran, dan Ian. Mereka telah bersahabat selama tujuh tahun dan sering menghabiskan waktu bersama di tempat tongkrongan favorit mereka, Lima sahabat ini memiliki sifat unik yang membuat persahabatan mereka  menjadi sangat menyenangkan.  Arial adalah sosok pria yang paling tampan dan sporty diantara mereka berlima. Kemudian Ada Riani yang merupakan seorang cewek yang kompetitif dan cerdas karena kesukaannya membaca dan belajar. Tokoh lainnya adalah Zafran yang digambarkan sebagai sosok puitis yang  sangat menggemari syair dan sastra. Lalu ada karakter Ian yang merupakan seorang fans fanatik sepak bola. Terakhir adalah Genta,  seorang aktivis kampus yang juga dianggap sebagai leader diantara mereka berlima. Cerita dimulai ketika Genta mengusulkan bahwa mereka tidak boleh bertemu dan berkomunikasi satu sama lain selama tiga bulan. Genta berpendapat jika mereka melakukan hal ini maka mereka bisa lebih fokus untuk mencapai goals pribadi mereka dan membenahi diri Pada awalnya, ide ini ditentang oleh Riani. Tapi, pada akhirnya mereka semua sepakat untuk melakukannya. Selama tiga bulan mereka tidak bertemu, banyak hal yang telah mereka lakukan.Ian sibuk  menyelesaikan skripsinya. Genta fokus pada pekerjaannya  di Event Organizer yang telah ditanganinya  selama ini. Sementara itu, Riani menyibukkan dirinya dengan bekerja. Lalu, ada Zafran yang memutuskan untuk memperdalam ilmu sastranya dan Arial yang akhirnya berhasil mendapatkan seorang kekasih Tiga bulan kemudian, mereka berlima pun akhirnya saling bertemu lagi di Stasiun Senen seperti yang telah ditentukan Genta.. Sebelumnya Genta juga telah menginstruksikan keempat kawannya untuk membawa peralatan tanpa  memberitahu apa alasannya.dan kemana mereka akan pergi. Di dalam kereta menuju ke Malang, Genta pun akhirnya memberikan bahwa ia akan mengajak mereka berlima untuk mendaki Puncak Mahameru.  Lantas, seperti apakah petualangan yang akan kunanti di mereka di Puncak Mahameru? Apakah mereka akan berhasil mendaki Puncak Mahameru? </p>
